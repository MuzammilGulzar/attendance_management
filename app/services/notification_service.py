def get_inbox(user, unread_only=False, limit=50):
    """
    Fetch notifications for any user (teacher, HOD, principal).
    Returns list of Notification objects, newest first.
    """
    q = user.notifications
    if unread_only:
        q = q.filter_by(is_read=False)
    return q.limit(limit).all()


def get_inbox_unread_count(user):
    """Quick count of unread notifications for navbar badge."""
    try:
        return user.notifications.filter_by(is_read=False).count()
    except Exception:
        return 0


def mark_read(user, notification_id):
    """Mark a single notification as read. Verifies ownership."""
    notif = Notification.query.get(notification_id)
    if not notif or notif.user_id != user.id:
        return False, 'Notification not found.'
    if notif.is_read:
        return True, None
    notif.mark_as_read()
    db.session.commit()
    return True, None


def mark_all_read(user):
    """Mark all unread notifications as read. Returns count marked."""
    unread = user.notifications.filter_by(is_read=False).all()
    now    = datetime.utcnow()
    for n in unread:
        n.is_read = True
        n.read_at = now
    if unread:
        db.session.commit()
    return len(unread)
