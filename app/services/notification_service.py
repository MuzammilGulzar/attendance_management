def get_sent_notifications(hod_user, limit=100):
    """
    Fetch all notifications sent BY this HOD (manual ones only).
    Used on the HOD's "Sent" page — shows what was broadcast and to whom.

    Returns list of dicts: {notification, recipient_name, is_read}
    """
    hod = _get_hod_teacher(hod_user)
    if not hod:
        return []

    sent = (
        Notification.query
        .filter_by(sent_by_id=hod_user.id, target_type='manual')
        .order_by(Notification.created_at.desc())
        .limit(limit)
        .all()
    )
    return sent


def get_sent_summary(hod_user):
    """
    Stats for the HOD notification dashboard:
      total_sent, read_count, unread_count
    """
    hod = _get_hod_teacher(hod_user)
    if not hod:
        return {}

    total  = Notification.query.filter_by(
        sent_by_id=hod_user.id, target_type='manual').count()
    read   = Notification.query.filter_by(
        sent_by_id=hod_user.id, target_type='manual', is_read=True).count()

    return {
        'total_sent'  : total,
        'read_count'  : read,
        'unread_count': total - read,
    }


